  CONSTRAINT _message_devis_fk__message FOREIGN KEY (id_message_devis) REFERENCES _message(id_message),
  CONSTRAINT _message_devis_fk__logement FOREIGN KEY (id_logement) REFERENCES _logement(id_logement)
